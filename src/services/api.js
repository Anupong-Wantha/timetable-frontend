export const scheduleAPI = {
  getAll: () => api.get('/schedule'),
  getById: (id, viewType = 'grid') => 
    api.get(`/schedule/${id}`, { params: { view: viewType } }),
  generate: (data) => api.post('/schedule/generate', data),
  delete: (id) => api.delete(`/schedule/${id}`),
  exportPDF: (id) => 
    api.get(`/schedule/${id}/export/pdf`, { responseType: 'blob' }),
  exportExcel: (id) => 
    api.get(`/schedule/${id}/export/excel`, { responseType: 'blob' }),
  getStatistics: () => api.get('/schedule/statistics'),
};

